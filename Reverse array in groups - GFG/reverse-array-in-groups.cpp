class Solution{
public:
    //Function to reverse every sub-array group of size k.
    void reverseInGroups(vector<long long>& a, int n, int k){
        for(int i=0;i<n;i+=k) {
            int sz = min(n-i,k);
            //cout<<sz<<endl;
            for(int j=i;j<i+sz/2;j++) {
                //cout<<j<<" "<<i+sz-1-(j-i)<<endl;
                swap(a[j],a[i+sz-1-(j-i)]);
            }
        }
    }
};

